//backspace button
function backspace() {
    var sl = str.length - 1
    str = str.slice(0, sl)

    document.getElementById("display1").value = str;
}

function space() {
    str = str + "  "
    document.getElementById("display1").value = str;
}

function speaker() {
    var msg = new SpeechSynthesisUtterance();
    var voices = window.speechSynthesis.getVoices();
    msg.voice = voices[4]
    msg.text = "  " + str + "  "
    msg.rate = 0.75
    window.speechSynthesis.speak(msg);
}



